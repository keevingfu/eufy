-- Paid KOL Management Table
-- 雇佣KOL管理表

CREATE TABLE IF NOT EXISTS paid_kol_management (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    kol_id TEXT,                          -- KOL唯一标识
    platform TEXT,                        -- 平台
    account_name TEXT,                    -- 账户名称
    account_url TEXT,                     -- 账户链接
    country TEXT,                         -- 国家/地区
    category TEXT,                        -- 类别(科技/生活方式/家居/美妆等)
    tier TEXT,                            -- 级别(Nano/Micro/Mid/Macro/Mega)
    
    -- 基础数据
    follower_count INTEGER,               -- 粉丝数量
    avg_views INTEGER,                    -- 平均观看量
    avg_engagement_rate REAL,             -- 平均互动率
    audience_demographics TEXT,           -- 受众人口统计(JSON格式)
    audience_interests TEXT,              -- 受众兴趣(JSON格式)
    audience_geography TEXT,              -- 受众地理分布(JSON格式)
    audience_quality_score REAL,          -- 受众质量评分(0-100)
    
    -- 合作信息
    collaboration_status TEXT,            -- 合作状态(潜在/进行中/历史/拒绝)
    first_contact_date DATE,              -- 首次联系日期
    last_contact_date DATE,               -- 最近联系日期
    collaboration_history TEXT,           -- 合作历史(JSON格式)
    contract_terms TEXT,                  -- 合同条款(JSON格式)
    exclusivity_terms TEXT,               -- 独家条款
    usage_rights TEXT,                    -- 使用权限
    
    -- 费用信息
    rate_card REAL,                       -- 标价
    negotiated_rate REAL,                 -- 协商价格
    payment_terms TEXT,                   -- 付款条款
    payment_history TEXT,                 -- 付款历史(JSON格式)
    cost_per_engagement REAL,             -- 每互动成本
    
    -- 内容信息
    content_specialties TEXT,             -- 内容专长
    content_quality_score REAL,           -- 内容质量评分(0-100)
    brand_alignment_score REAL,           -- 品牌一致性评分(0-100)
    authentic_presentation_score REAL,    -- 真实呈现评分(0-100)
    key_messaging_score REAL,             -- 关键信息传达评分(0-100)
    
    -- 表现评估
    historical_performance TEXT,          -- 历史表现(JSON格式)
    campaign_results TEXT,                -- 活动结果(JSON格式)
    avg_conversion_rate REAL,             -- 平均转化率
    avg_roi REAL,                         -- 平均ROI
    competitive_analysis TEXT,            -- 竞争分析(JSON格式)
    
    -- 关系管理
    relationship_manager TEXT,            -- 关系管理员
    communication_preference TEXT,        -- 沟通偏好
    response_time_avg TEXT,               -- 平均响应时间
    creator_reliability_score REAL,       -- 创作者可靠性评分(0-100)
    escalation_history TEXT,              -- 问题升级历史
    
    -- 战略评估
    strategic_value_score REAL,           -- 战略价值评分(0-100)
    future_collaboration_potential TEXT,  -- 未来合作潜力
    growth_trajectory TEXT,               -- 增长轨迹
    risk_assessment TEXT,                 -- 风险评估
    
    notes TEXT,                           -- 备注
    last_updated DATE,                    -- 最后更新日期
    update_timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP -- 更新时间戳
);

