-- Content Trends Analysis Table
-- 内容趋势分析表

CREATE TABLE IF NOT EXISTS content_trends_analysis (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    trend_name TEXT,                     -- 趋势名称
    trend_category TEXT,                 -- 趋势类别(内容形式/主题/技术/社交互动/叙事方式)
    platform TEXT,                       -- 平台
    first_observed DATE,                 -- 首次观察日期
    peak_date DATE,                      -- 峰值日期
    current_status TEXT,                 -- 当前状态(上升期/稳定期/衰退期/完结)
    trend_score REAL,                    -- 趋势强度评分(0-100)
    
    -- 趋势特征
    key_characteristics TEXT,            -- 关键特征
    typical_duration TEXT,               -- 典型持续时间
    audience_demographics TEXT,          -- 主要受众特征(JSON格式)
    content_format TEXT,                 -- 内容格式
    technical_requirements TEXT,         -- 技术要求
    
    -- 表现指标
    avg_views REAL,                      -- 平均观看量
    avg_engagement_rate REAL,            -- 平均互动率
    avg_sharing_rate REAL,               -- 平均分享率
    viral_potential REAL,                -- 病毒传播潜力(0-5)
    brand_fit_score REAL,                -- 品牌契合度(0-5)
    commercial_value REAL,               -- 商业价值(0-5)
    
    -- 竞争分析
    competitor_adoption TEXT,            -- 竞争对手采用情况(JSON格式)
    industry_relevance TEXT,             -- 行业相关性
    first_mover_advantage REAL,          -- 先发优势评分(0-5)
    
    -- 实施建议
    adoption_recommendation TEXT,        -- 采用建议
    optimal_execution TEXT,              -- 最佳执行方式
    risk_factors TEXT,                   -- 风险因素
    resources_required TEXT,             -- 所需资源
    expected_roi TEXT,                   -- 预期投资回报
    
    -- 示例与案例
    trending_examples TEXT,              -- 流行案例(JSON格式)
    best_practice_reference TEXT,        -- 最佳实践参考
    eufy_specific_adaptation TEXT,       -- Eufy特定改编建议
    
    -- 长期评估
    trend_lifecycle_stage TEXT,          -- 趋势生命周期阶段
    sustainability_score REAL,           -- 可持续性评分(0-5)
    long_term_impact TEXT,               -- 长期影响分析
    
    notes TEXT,                          -- 备注
    analyst TEXT,                        -- 分析师
    last_updated DATE,                   -- 最后更新日期
    update_timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP -- 更新时间戳
);

